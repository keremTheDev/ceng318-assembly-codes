.MODEL SMALL
.STACK 100H

.DATA
DATA1 DB 'CAGLA SUI'
DATA2 DB 9 DUP(?)

.CODE 
MAIN PROC
    
    MOV AX,@DATA
    MOV DS,AX
    
    MOV CX,9
    MOV SI,OFFSET DATA1
    MOV DI,OFFSET DATA2
BACK:
    MOV AL,[SI]  
    CMP AL,41H
    JB OVER
    CMP AL,5AH
    JA OVER
    OR AL,00100000B
OVER:
    MOV [DI],AL
    INC SI
    INC DI
    LOOP BACK
    
    MOV AH,4CH
    INT 21H
ENDP
    END MAIN