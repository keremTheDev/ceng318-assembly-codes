MULTP MACRO NUM1,NUM2,RESULT
    LOCAL ADD_LOOP
    MOV CX,NUM2
    
ADD_LOOP:
    MOV AX,NUM1
    ADD RESULT,AX
    LOOP ADD_LOOP
ENDM

.MODEL SMALL
.STACK 64

.DATA
NUM1 DW 5
NUM2 DW 4
RESULT DW ?

.CODE

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    MULTP NUM1,NUM2,RESULT
    MOV BX,RESULT
    
    MOV AH,4CH
    INT 21H
    MAIN ENDP
END MAIN
    
    
    
