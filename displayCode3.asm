

CLEARSCR MACRO
    MOV AX,0600H
    MOV BH,07
    MOV CX,0
    MOV DX,184FH
    INT 10H
ENDM

CURSOR MACRO ROW,COLUMN
    MOV AH,02
    MOV BH,00
    MOV DH,ROW
    MOV DL,COLUMN
    INT 10H
ENDM

DISP MACRO STRING
    MOV AH,09
    MOV DX,OFFSET STRING
    INT 21H
ENDM

.MODEL SMALL
.STACK 64

.DATA

MESSAGE1 DB 'My name','$'
MESSAGE2 DB 'is Joe','$'
MESSAGE3 DB 'what is' ,'$'
MESSAGE4 DB 'your name?','$'

.CODE
MAIN PROC 
    MOV AX,@DATA
    MOV DS,AX
    
    CLEARSCR
    CURSOR 2,4
    DISP MESSAGE1
    CURSOR 7,24
    DISP MESSAGE2
    CURSOR 12,44
    DISP MESSAGE3
    CURSOR 19,64
    DISP MESSAGE4
    
    MOV AH,4CH
    INT 21H
ENDP
END MAIN